    {
        $descarga = new descargacurso();
        $descarga->email = $request->email;
        $descarga->curso =$request->curso;
        $descarga->save();

        $curso = DB::table('cursos')->where('titulo','=',$request->curso)->get();
       
        return response()->download('programas/'.$curso[0]->link_programa);
        return redirect()->back();
    }

